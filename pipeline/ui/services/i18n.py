def get_lang() -> str:
    return st.session_state.get("lang", "en")


def t(key: str, **fmt) -> str:
    lang = get_lang()
    val = STRINGS.get(lang, {}).get(key) or STRINGS["en"].get(key, key)
    if fmt:
        try:
            return val.format(**fmt)
        except Exception:
            return val
    return val


def language_toggle() -> None:
    """Render the sidebar language selector. Call from each page."""
    cur = get_lang()
    pick = st.sidebar.radio(
        t("sidebar.lang"),
        options=["en", "vi"],
        index=0 if cur == "en" else 1,
        format_func=lambda x: "🇬🇧 English" if x == "en" else "🇻🇳 Tiếng Việt",
        horizontal=True,
        key="lang_radio",
    )
    if pick != cur:
        st.session_state["lang"] = pick
        st.rerun()
